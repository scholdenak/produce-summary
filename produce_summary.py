def produce_daily_summary(file, day):

    print(day)
    the_file = open(file)
    for line in the_file:
        line = line.rstrip()
        words = line.split('|')

        melon = words[0]
        count = words[1]
        amount = words[2]

        print(f"Delivered  {count} {melon}s for total of ${amount}.")
    
    the_file.close()

produce_daily_summary("um-deliveries-20140519.txt", "Day 1")

